     private  final void setTableView(){
        int[] width={40,100};
        for(int i=0;i<titleTable.length;i++){
            jTable1.getColumnModel().getColumn(i).setPreferredWidth(width[i]);
        }
     }
    
     private void getAllKelas(){
       try{
       String sql ="select * from kelas order by CAST(nama_kelas as SIGNED INTEGER) ASC";
       rs = st.executeQuery(sql); // meload query
       
       ResultSetMetaData md = rs.getMetaData();
       int kolom = md.getColumnCount();
       kolom+=1; // jumlah kolom ditambahkan untuk menambahkan dataKelas[x][2]
       int baris = 0;
       while(rs.next()){
           baris = rs.getRow(); // dapatkan jumlah datanya.
       }
       dataKelas = new Object[baris][kolom]; // Buat objek sebanyak jumlah data
       
       // mulai memasukan data pada objek
       int x = 0;
       rs.beforeFirst();
       while(rs.next()){
           dataKelas[x][0] = x+1; // untuk nomor
           dataKelas[x][1] = rs.getString("nama_kelas");
           dataKelas[x][2] = rs.getInt("id_kelas"); // tidak dimunculkan pada tabel, tapi ditransfer ke int id_kelas
           x++;
       }
       jTable1.setModel(new DefaultTableModel(dataKelas,titleTable));
       }
       catch(SQLException ex){} 
     }
     
     private void getDataToForm(){
       int row = jTable1.getSelectedRow();
       setDapatkanIdKelas(row);
       jTextField1.setText((String)jTable1.getValueAt(row, 1));      
     }
     
     public void setDapatkanIdKelas(int row){
      id_kelas = (int) dataKelas[row][2];
     }
     
     private void clear(){
        jTextField1.setText(null);
        id_kelas=0;
        Save.setEnabled(true);
        Edit.setEnabled(false);
        Delete.setEnabled(false);
     }
   
  
   
     private void cekTextField(){
       nama_kelas = jTextField1.getText();
       if(nama_kelas.isEmpty()){
           JOptionPane.showMessageDialog(null,"Nama Kelas masih kosong");
           jTextField1.requestFocus();
       }
       else{formkeisisemua=true;}
     }
   
   
     private void delete(){
       try{
       String sql = "DELETE from kelas WHERE id_kelas='"+id_kelas+"'";
       st.executeUpdate(sql);
       getAllKelas();
       clear();
       }
       catch(SQLException ex){}
     }
    
     private void edit(){
        cekTextField();
       if(formkeisisemua==true){
           try{
           String sql = "UPDATE kelas SET nama_kelas='"+nama_kelas+"' "
                   + "WHERE id_kelas='"+id_kelas+"'";
           st.executeUpdate(sql);
           getAllKelas();
           clear();
           formkeisisemua=false;
           }
           catch(SQLException ex){}
       }
     }
   
     private void save(){
       cekTextField();
       if(formkeisisemua==true){
           try{
           String sql = "INSERT INTO kelas (nama_kelas) "
                   + "VALUES ('"+nama_kelas+"')";
           st.executeUpdate(sql);
           getAllKelas();
           clear();
           formkeisisemua=false;
           }
           catch(SQLException ex){}
       }
     }

